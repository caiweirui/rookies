#include<stdio.h>
main()
{
	int K,a,b,c,d,e,x,y,z,n;
	scanf("%d",&K);
	for(n=10000;n<=30000;n++)
	{
		a=n%10;
		b=((n-a)/10)%10;
		c=((n-a-10*b)/100)%10;
		d=((n-a-10*b-100*c)/1000)%10;
		e=((n-a-10*b-100*c-1000*d)/10000)%10;
		x=e*100+d*10+c;
		y=d*100+c*10+b;
		z=c*100+b*10+a;
		if(x%K==0&&y%K==0&&z%K==0)
		{
			printf("%d\n",n);
		}
	}
}